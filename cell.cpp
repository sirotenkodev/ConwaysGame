#include "cell.h"

//-----------------------------------------------------------------------------
Cell::Cell()
    : m_isAlive{false}
    , m_neighbor{0}
{}

//-----------------------------------------------------------------------------
void Cell::born()
{
    m_isAlive = true;
}

//-----------------------------------------------------------------------------
void Cell::die()
{
    m_isAlive = false;
}

//-----------------------------------------------------------------------------
bool Cell::isAlive()
{
    return m_isAlive;
}

//-----------------------------------------------------------------------------
void Cell::addNeighbor()
{
    ++m_neighbor;
}

//-----------------------------------------------------------------------------
int Cell::getNeighbor()
{
    return m_neighbor;
}

//-----------------------------------------------------------------------------
void Cell::clearNeighbor()
{
    m_neighbor = 0;
}
